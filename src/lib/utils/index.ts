/**
 * Creates a debounced version of a function that delays invoking until after wait milliseconds
 * have elapsed since the last time the debounced function was invoked.
 * @param func - The function to debounce
 * @param wait - The number of milliseconds to delay
 * @returns The debounced function
 * @example
 * const debouncedFn = debounce(() => console.log('Hello'), 1000);
 * debouncedFn(); // Will only execute after 1 second of no more calls
 */
export function debounce<T extends (...args: unknown[]) => unknown>(
  func: T,
  wait: number = 1500
): ((...args: Parameters<T>) => void) & { cancel: () => void } {
  // Use ReturnType<typeof setTimeout> so it works in both browser and Node environments
  let timeout: ReturnType<typeof setTimeout> | undefined;

  const executedFunction = (...args: Parameters<T>) => {
    const later = () => {
      if (timeout !== undefined) clearTimeout(timeout);
      func(...args);
    };

    if (timeout !== undefined) clearTimeout(timeout);
    timeout = setTimeout(later, wait);
  };

  // Expose a cancel method so callers can clear the pending timeout if needed
  executedFunction.cancel = () => {
    if (timeout !== undefined) {
      clearTimeout(timeout);
      timeout = undefined;
    }
  };

  return executedFunction as ((...args: Parameters<T>) => void) & { cancel: () => void };
}

/**
 * Función para analizar fechas en formato ISO 8601
 * Convierte una cadena de fecha en un objeto Date y devuelve una representación
 * en formato de fecha legible (por ejemplo, "Jan 1, 2020")
 * Si la fecha no es válida, devuelve undefined.
 * @param dateString - Cadena de fecha en formato ISO 8601
 * @return Una cadena de fecha formateada o undefined si la fecha es inválida
 */
export const parseDate = (dateString: string): string | undefined => {
  const date = new Date(dateString);
  return !isNaN(date.getTime())
    ? date.toLocaleDateString("es-AR", {
        year: "numeric",
        month: "short",
        day: "numeric",
      })
    : undefined;
};

/**
 * Función para simular una demora en la ejecución
 * @param ms - Tiempo en milisegundos para la demora
 * @returns Una promesa que se resuelve después de la demora especificada
 * @example
 * // Uso de la función delay
 * await sleep(1000); // Espera 1 segundo
 */
export const sleep = (ms: number = 500) => new Promise((resolve) => setTimeout(resolve, ms));

/**
 * Función para formatear un monto numérico a una cadena con formato monetario en pesos argentinos.
 * @param amount - Monto a formatear
 * @returns Una cadena con el monto formateado en pesos argentinos (por ejemplo, "$1.234,56")
 */
export const parseMoney = (amount: number | undefined): string => {
  if (amount === undefined) return "$0.00";
  return `$${amount.toLocaleString("es-AR", {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  })}`;
};

