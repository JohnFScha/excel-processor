import { clsx, type ClassValue } from "clsx";
import { twMerge } from "tailwind-merge";

/**
 * Combines class names into a single string, merging duplicates and handling conditional classes.
 * This function is useful for dynamically constructing class names in React components.
 * @param inputs - An array of class names or conditional class names.
 * @returns A single string with all class names merged and duplicates removed.
 */
export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

